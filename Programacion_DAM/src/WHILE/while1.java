package WHILE;

import java.util.Scanner;

public class while1 {

	public static void main(String[] args) {
		
		int count1=1;
	
		
		
		
		while (count1<=25) {
			
			
			if (count1% 5==0 ) {
				System.out.println("*");
				count1++;
								
			}else {
				System.out.print("*");
				count1++;
			}
		}

	}

}
